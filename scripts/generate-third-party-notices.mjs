/**
 * Generates THIRD-PARTY-NOTICES for @microsoft/spe-mcp by walking the
 * resolved *production* dependency tree (`npm ls --omit=dev --all --json`) and
 * collecting each package's declared license plus its license text from
 * node_modules. Run after any production-dependency change:
 *
 *   npm run notices
 *
 * This script is a dev tool and is intentionally NOT in the package `files`
 * allow-list, so it is not published in the tarball.
 */
import { execSync } from "node:child_process";
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const pkgRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const outFile = join(pkgRoot, "THIRD-PARTY-NOTICES");

const LICENSE_FILE_RE = /^(LICENSE|LICENCE|COPYING|NOTICE)(\.|$)/i;

/** Run `npm ls` and tolerate a non-zero exit (npm exits 1 on extraneous/peer warnings). */
function readProductionTree() {
  try {
    const out = execSync("npm ls --omit=dev --all --json", {
      cwd: pkgRoot,
      encoding: "utf8",
      maxBuffer: 64 * 1024 * 1024,
      stdio: ["ignore", "pipe", "ignore"],
    });
    return JSON.parse(out);
  } catch (err) {
    if (err.stdout) return JSON.parse(err.stdout.toString());
    throw err;
  }
}

/** Recursively collect unique production packages keyed by name@version. */
function collect(node, name, acc) {
  if (!node || typeof node !== "object") return;
  if (name && node.version) {
    const key = `${name}@${node.version}`;
    if (!acc.has(key)) {
      acc.set(key, {
        name,
        version: node.version,
        path: node.path || join(pkgRoot, "node_modules", name),
      });
    }
  }
  for (const [depName, depNode] of Object.entries(node.dependencies || {})) {
    collect(depNode, depName, acc);
  }
}

function readJson(file) {
  try {
    return JSON.parse(readFileSync(file, "utf8"));
  } catch {
    return null;
  }
}

function normalizeLicense(manifest) {
  if (!manifest) return "UNKNOWN";
  if (typeof manifest.license === "string") return manifest.license;
  if (manifest.license && typeof manifest.license === "object" && manifest.license.type) {
    return manifest.license.type;
  }
  if (Array.isArray(manifest.licenses)) {
    return manifest.licenses.map((l) => (typeof l === "string" ? l : l.type)).join(", ");
  }
  return "UNKNOWN";
}

function readLicenseText(pkgPath) {
  if (!existsSync(pkgPath)) return null;
  let entries;
  try {
    entries = readdirSync(pkgPath);
  } catch {
    return null;
  }
  const file = entries.find((e) => LICENSE_FILE_RE.test(e));
  if (!file) return null;
  try {
    return readFileSync(join(pkgPath, file), "utf8").trim();
  } catch {
    return null;
  }
}

function repoUrl(manifest) {
  if (!manifest) return "";
  if (typeof manifest.repository === "string") return manifest.repository;
  if (manifest.repository && manifest.repository.url) {
    return manifest.repository.url.replace(/^git\+/, "").replace(/\.git$/, "");
  }
  return manifest.homepage || "";
}

const tree = readProductionTree();
const rootName = tree.name;
const collected = new Map();
for (const [depName, depNode] of Object.entries(tree.dependencies || {})) {
  collect(depNode, depName, collected);
}

const packages = [...collected.values()]
  .filter((p) => p.name !== rootName)
  .sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));

const SEP = "-".repeat(63);
const header = `NOTICES AND INFORMATION
Do Not Translate or Localize

This software incorporates material from third parties. Microsoft makes certain
open source code available at https://3rdpartysource.microsoft.com, or you may
send a check or money order for US $5.00, including the product name, the open
source component name, and version number, to:

  Source Code Compliance Team
  Microsoft Corporation
  One Microsoft Way
  Redmond, WA 98052
  USA

Notwithstanding any other terms, you may reverse engineer this software to the
extent required to debug changes to any libraries licensed under the GNU Lesser
General Public License.

This file lists the third-party production dependencies of ${rootName} and their
licenses. It is generated by scripts/generate-third-party-notices.mjs.
`;

const blocks = packages.map((p, i) => {
  const manifest = readJson(join(p.path, "package.json"));
  const license = normalizeLicense(manifest);
  const url = repoUrl(manifest);
  const text = readLicenseText(p.path);
  const lines = [
    SEP,
    "",
    `${i + 1}. ${p.name} ${p.version} (${license})`,
  ];
  if (url) lines.push(url);
  lines.push("");
  lines.push(text || `License: ${license} (no bundled license text found).`);
  lines.push("");
  return lines.join("\n");
});

const body = `${header}\n${blocks.join("\n")}\n${SEP}\n`;
writeFileSync(outFile, body, "utf8");
console.log(`Wrote ${outFile} with ${packages.length} third-party packages.`);
